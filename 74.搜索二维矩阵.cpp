// @lc code=start

#include <vector>
#include <algorithm>
using namespace std;

class Solution {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        for(auto &line : matrix)
        {
            int pos = lower_bound(line.begin(), line.end(), target) - line.begin();
            if(pos < line.size() && line[pos] == target)
                return true;
        }
        return false;
    }
};
